// 연산자란, 우리가 아는 그 연산을 하는 자 이다.
// 주로 +(덧셈), -(뺄셈), *(곱셈), /(나눗셈), %(나눈 후 나머지) 가 많이 쓰인다.

public class operator {
    public static void main(String[] args) {
        int a = 2134;
        int b = 1234;

        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a % b);
    }
}