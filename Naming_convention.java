// Java 의 명명규칙

/*
Java 의 명명규칙(이름 짓는거)

1. True 와 true 는 다름. 대소문자를 가림, 길이의 제한은 없음

2. 첫 글자가 숫자이면 안된다.

    int a10 = 10; 은 되지만
    int 10a = 10; 은 안된다.

3. 특수문자는 _ 와 $ 만 허용된다.

    int a_b = 10 + 13;
    int a$b = 10 + 13;

4. 클래스 이름은 대문자로 시작하여야 한다.

    class Main {
    }

5. 여러 단어 이름은 첫 문자를 대문자로 한다.

    int YouAreBabo = 213;

6. 상수의 이름은 대문자로 해야 한다.
    final int YOU_ARE_BABO = 1234;

 */